    private void tf_valorKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tf_valorKeyTyped
        // TODO add your handling code here:
        if (list_filtros.getSelectedItem()=="CÃ³digo Consumo" || list_filtros.getSelectedItem()=="Reserva" ||
               list_filtros.getSelectedItem()=="Total" ){
            ch=evt.getKeyChar();
            if(!Character.isDigit(ch)){
                getToolkit().beep();
                evt.consume();
            }
        }
        else{
            ch=evt.getKeyChar();
            if(Character.isDigit(ch)){
                getToolkit().beep();
                evt.consume();
            }
        }
    }//GEN-LAST:event_tf_valorKeyTyped

    private void btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarActionPerformed
        // TODO add your handling code here:
        // TODO add your handling code here:
        int i=0;
        int sum=0;
       if (tf_valor.getText().length()==0){
            JOptionPane.showMessageDialog(null,"Ingrese algÃºn valor para efectuar la bÃºsqueda", "Error",JOptionPane.ERROR_MESSAGE);
            return;
        }else{
            if (list_filtros.getSelectedItem()=="CÃ³digo Consumo"){
                Query = EntityManager.createNamedQuery("ConsumoProSer.findByCodigoConsumo");
                Query.setParameter("codigoConsumo", Integer.parseInt(tf_valor.getText()));
                List<ConsumoProSer> c = Query.getResultList();
                if (c.isEmpty()){
                    JOptionPane.showMessageDialog(null,"CÃ³digo de Consumo inexistente", "Error",JOptionPane.ERROR_MESSAGE);
                    tf_valor.setText(null);
                    return;
                }
                List.clear();
                List.addAll(c);
            }
            else if (list_filtros.getSelectedItem()=="Producto/Servicio"){
                Query = EntityManager.createNativeQuery( "SELECT * FROM consumo_pro_ser c "
                    + "INNER JOIN producto_servicio p "
                    + "on c.codigoPS = p.codigoPS "
                    + "WHERE p.nombre LIKE "
                    +"'%"+tf_valor.getText()+"%'", ConsumoProSer.class);
                List<ConsumoProSer> c = Query.getResultList();
                if (c.isEmpty()){
                    JOptionPane.showMessageDialog(null," Producto/Servicio Inexistente", "Error",JOptionPane.ERROR_MESSAGE);
                    tf_valor.setText(null);
                    return;
                }
                List.clear();
                List.addAll(c);
            }
            else if (list_filtros.getSelectedItem()=="Total"){
                Query = EntityManager.createNativeQuery( "SELECT * FROM consumo_pro_ser "
                    + "WHERE total>= "
                    +"'"+tf_valor.getText()+"'", ConsumoProSer.class);
                List<ConsumoProSer> c = Query.getResultList();
                if (c.isEmpty()){
                    JOptionPane.showMessageDialog(null, "Nombre de Producto/Servicio inexistente","Error",JOptionPane.ERROR_MESSAGE );
                    tf_valor.setText(null);
                    return;
                }
                List.clear();
                List.addAll(c);
                
            }
            else if (list_filtros.getSelectedItem()=="Reserva"){
                Query = EntityManager.createNativeQuery( "SELECT * FROM consumo_pro_ser c "
                    +"WHERE c.codigoReserva= "
                    +"'"+tf_valor.getText()+"'", ConsumoProSer.class);
                List<ConsumoProSer> c = Query.getResultList();
                if (c.isEmpty()){
                    JOptionPane.showMessageDialog(null, "CÃ³digo Reserva inexistente","Error",JOptionPane.ERROR_MESSAGE );
                    tf_valor.setText(null);
                    return;
                }
                List.clear();
                List.addAll(c);
                for(i=0;i<List.size();i++){
                    sum=sum+List.get(i).getTotal();
                }
                lbl_total.setEnabled(true);
              
                tf_total.setText(Integer.toString(sum));
                btn_factura.setEnabled(true);
            }
        }
    }//GEN-LAST:event_btn_buscarActionPerformed

